#ifndef __TIME_H__
#define __TIME_H__

#include "Kernel\Sys.h"

// 时间类
// 使用双计数时钟，TIMx专用于毫秒级系统计时，SysTick用于微秒级延迟，秒级以及以上采用全局整数累加
// 这样子可以避免频繁使用微秒时带来长整型乘除法
class TTime
{
public:
	volatile	uint	Seconds;		// 全局秒数，系统启动后总秒数。累加
	volatile	UInt64	Milliseconds;	// 全局毫秒数，系统启动后总毫秒（1000ms整部分）。
	volatile	uint	BaseSeconds;	// 基准秒数。系统启动时相对于1970年的秒数，时间调节，加上Seconds得到当前时间Now()
    //byte	Ticks;			// 每微秒的时钟滴答数
	byte	Index;			// 定时器
#if ! (defined(STM32F0) || defined(GD32F150))
	byte	Div;			// 分频系数。最大分频64k，无法让大于64M主频的芯片分配得到1k时钟
#endif

    TTime();

	void Init();

    uint CurrentTicks() const;	// 当前滴答时钟

	uint TicksToUs(uint ticks) const;
	uint UsToTicks(uint us) const;
};

void delayMicroseconds(uint us);//us延时，100us以下精确
void delay(UInt64 ms);//延时毫秒
UInt64 millis();//系统启动后的毫秒数
UInt64 micros();//开机到现在的微妙数

extern TTime Time;
// 时间开支。借助滴答进行精确计算
class TimeCost
{
public:
	UInt64	Start;		// 开始时间，毫秒
	uint	StartTicks;	// 开始滴答

	TimeCost();

	void Reset();
	int Elapsed() const;	// 逝去的时间，微秒
	void Show(cstring format = nullptr) const;
};

/*
开发历史：

2015-10-05
系统最频繁使用的函数Current()存在除法性能问题，原采用滴答定时器8分频，若干个滴答才得到一个微秒，必然存在一个除法运算。
而任务调度系统等各个地方大量调用Current以获取当前时间，特别是中断函数内部直接或间接调用，造成极大的性能损耗。
根据黄总@405803243的指点，修改为双定时器架构。
系统时钟采用基本定时器，任意分频，很方便凑出每个计数表示1毫秒，彻底根除Current除法问题。
另外，滴答定时器用于微秒级高精度延迟，应用于硬件驱动开发等场合。
*/

#endif
