//高8位的数据
void OCM240128::writedata(byte da)
{
	ocmd0 = da & (1 << 0);
	ocmd1 = da & (1 << 1);
	ocmd2 = da & (1 << 2);
	ocmd3 = da & (1 << 3);
	ocmd4 = da & (1 << 4);
	ocmd5 = da & (1 << 5);
	ocmd6 = da & (1 << 6);
	ocmd7 = da & (1 << 7);
}

