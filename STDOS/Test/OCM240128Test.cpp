//高8位的数据
void OCM240128::LCD_WriteData(byte da)
{
	LCD_DATA_GPIO->BSRR = da << 8 & 0xff00; 
	LCD_DATA_GPIO->BRR = ((~da) << 8) & 0xff00;
}

